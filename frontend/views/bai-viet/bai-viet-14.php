                            <p>Dù mùa giải còn chưa khép lại song ngay từ lúc này, tiền vệ sinh năm 1992 đã nhận được hàng loạt đề nghị hấp dẫn từ loạt CLB khác nhau.</p>
                            <p>Ở phía Bắc, đại gia Viettel được xác nhận đã có động thái tiếp cận và giành "pole" trong việc chào mời chữ ký của ngôi sao xứ Nghệ. </p>
                           <p>Dù chưa chính thức ký kết song có thể khẳng định đến 90%, tiền vệ này sẽ rời quê nhà để đầu quân cho đại diện Thủ đô từ năm 2020.</p>
                            <figure class="post-thumb"><img src="img/thumb/khacngoc.jpg" alt=""> <center><p class="caption-text">Khắc Ngọc chia tay Sông Lam bằng mùa giải với nhiều bàn thắng nhất sau 8 năm lên đội 1</p></center></figure>
                            <p>TKhắc Ngọc bắt đầu xuất hiện ở đội 1 từ mùa giải 2012. Song cũng phải mất đến 4 năm học hỏi và rèn giũa bản thân, anh mới dần chứng minh được khả năng và có đươc chỗ đứng. Khán giả ấn tượng với Ngọc bởi lối chơi kỹ thuật, nhãn quan chiến thuật sắc bén, săn bàn lẫn kiến tạo đều rất ổn. </p>
                            <p>Mùa này, anh đã có 4 pha lập công cùng 3 kiến tạo cho đội nhà. Các khoảnh khắc tỏa sáng này đã góp phần không nhỏ giúp SLNA đặt một chân trong top 4 đến hiện tại, và tràn trề hi vọng kết thúc mùa giải với một tấm huy chương. Nếu được vậy, đó sẽ là lời chia tay đẹp của Khắc Ngọc với quê nhà.</p>

                            <p>Ngoài Khắc Ngọc, SLNA sẽ đáo hạn hợp đồng với... 13 cầu thủ. Đáng chú ý trong số này bao gồm thủ môn Nguyên Mạnh, hậu vệ cánh Đình Hoàng, tiền đạo Michael Olaha (dự đoán đã ký nháy với CLB Israel). Nếu không sớm giải bài toán tài chính, mùa tới hứa hẹn sẽ rất nhiều cam go với bóng đá xứ Nghệ!</p>
