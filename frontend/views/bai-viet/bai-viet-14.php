                            <h1 class="post-title">Khắc Ngọc đứng trước tin đồn rời SLNA chuyển đến CLB Viettel</h1>

                            <h5>Mùa giải V.League 2019 chưa khép lại nhưng hai cầu thủ Đình Hoàng và Ngọc Toàn đã đạt được thỏa thuận gia nhập đội bóng mới.</h5>
                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
