                        <figure class="post-thumb"><img src="img/thumb/cnshbdanang.jpg" alt=""><center><p class="caption-text">Vào trưa nay (1/10), báo chí trong nước bất ngờ loan tin CLB Viettel đã đạt được thỏa thuận chiêu mộ tiền vệ Hồ Khắc Ngọc từ mùa giải 2020.</p></center></figure>
                        <p><span class="drop-caps">S</span>ắp đáo hạn hợp đồng với Sông Lam Nghệ An vào cuối V.League 2019, Khắc Ngọc dĩ nhiên trở thành món hàng "hot" trên TTCN.</p>
                        <p></p>

                        <div class="entry-content">
                            <div class="news-auth pull-right">
                                <div class="auth-des">
                                    <figure>
                                        <img src="img/auth/tacgia.jpg" width="50" height="50" alt="">
                                    </figure>
                                    <h5>Tính Trần</h5>
                                    <h6>Tác Giả</h6>
                                </div>
                                <div class="auth-links social--color">
                                    <a class="social__facebook" href="#"><i class="fa fa-facebook"></i>Facebook</a>
