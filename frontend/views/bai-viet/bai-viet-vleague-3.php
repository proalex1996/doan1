                        <p><span class="drop-caps">H</span>ôm nay, CLB Thanh Hóa hoàn tất hợp đồng với HLV Fabio Lopez. HLV Lopez sinh năm 1973, là cựu thủ môn của đội trẻ AS Roma rồi làm HLV thủ môn tại CLB này.</p>
                        <p>Tại châu Á, HLV Lopez từng làm việc tại  các giải vô địch Ả Rập Xê Út, Oman, Malaysia, Indonesia... Ông từng dẫn dắt đội tuyển Bangladesh giai đoạn 2015-2016. </p>
                        <figure class="post-thumb"><img src="img/thumb/thanhhoa.jpg" alt=""><center><p class="caption-text"></p></center></figure>

                            <p>Sau khi giữ được tấm vé ở lại V-League bằng chiến thắng trước Phố Hiến trong trận play-off, Bầu Đệ tuyên bố sẽ "làm lại" ở mùa giải này. Và HLV Lopez nằm trong chuỗi tăng cường lực lượng khá ấn tượng gần 1 tháng qua của Thanh Hoa, với những tân binh được đánh giá cao như Hoàng Vũ Samson, Louis Epassi Ewonde, Trịnh Văn Lợi.</p>
