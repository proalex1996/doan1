                                Cựu HLV tại AS Roma làm HLV trưởng CLB Thanh Hoá
                            </h1>

                            <h5>Sau 1 mùa giải bết bát, Thanh Hoá bổ nhiệm HLV trưởng mới, ông Fabio Lopez.</h5>

                            <div class="post-meta">
