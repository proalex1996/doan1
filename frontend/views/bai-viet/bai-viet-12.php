                            <h1 class="post-title">Tin Hà Nội FC: Đức Huy nói gì trước tin đồn chuyển tới CLB Chonburi?</h1>

                            <h5>Tiền vệ của ĐT Việt Nam khẳng định vẫn đang dành ưu tiên cho Hà Nội FC trước khi nghĩ tới vấn đề tương lai.</h5>
                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
                                </div>

                            </div>
                        </div>
                        <figure class="post-thumb"><img src="img/thumb/duchuy.jpg" alt=""> <center><p class="caption-text">Tiền vệ của ĐT Việt Nam khẳng định vẫn đang dành ưu tiên cho Hà Nội FC trước khi nghĩ tới vấn đề tương lai.</p></center></figure>
                        <p><span class="drop-caps">T</span>heo truyền thông Thái Lan, Phạm Đức Huy đang nhận được sự quan tâm đặc biệt từ CLB Chonburi. The Sharks đã chìa ra mức thu nhập "kếch sù" để chào mời cầu thủ sẽ đáo hạn hợp đồng đào tạo với Hà Nội FC sau khi V.League 2019 khép lại.</p>


                        <div class="entry-content">
                            <div class="news-auth pull-right">
                                <div class="auth-des">
                                    <figure>
                                        <img src="img/auth/tacgia.jpg" width="50" height="50" alt="">
                                    </figure>
                                    <h5>Tính Trần</h5>
                                    <h6>Tác Giả</h6>
                                </div>
                                <div class="auth-links social--color">
                                    <a class="social__facebook" href="#"><i class="fa fa-facebook"></i>Facebook</a>
                                    <a class="social__twitter" href="#"><i class="fa fa-twitter"></i>Twitter</a>
                                    <a class="social__google-plus" href="#"><i class="fa fa-google-plus"></i>Google Plus</a>
                                </div>
                            </div>

                            <p>Cụ thể, đội bóng Thái Lan sẵn sàng trả mức lương gần 10.000 USD/tháng kèm theo phí "lót tay" 100.000 USD/mùa để thuyết phục Đức Huy. Tổng cộng, khoản thu nhập hàng năm của tiền vệ này có thể lên tới 220.000 USD (hơn 5 tỷ VNĐ).</p>
                            <p>Trước những thông tin này, anh cho biết vẫn muốn dành sự tập trung tối đa cho phần còn lại của mùa giải với Hà Nội FC, đặc biệt là trận bán kết cúp Quốc gia với TP HCM vào ngày 27/10 tới, hay xa hơn là 2 trận đấu của ĐT Việt Nam vào tháng 11/2019, gặp ĐT UAE và ĐT Thái Lan.</p>
                            <figure class="post-thumb"><img src="img/thumb/duchuy1.jpg" alt=""> <center><p class="caption-text">Đức Huy có thể nhận lương "khủng" nếu tới Thái Lan.</p></center></figure>

                            <p>Dân Trí dẫn lời Đức Huy: "Tôi muốn tập trung cho những trận đấu còn lại cùng Hà Nội FC và hai trận cùng ĐT Việt Nam, sau đó mới có thể nói về tương lai của mình.”</p>
                            <p>Trợ lý HLV Nguyễn Công Tuấn của Hà Nội FC chia sẻ: "Cầu thủ Việt Nam hay thế giới đều có thương hiệu riêng. Họ sắp hết hợp đồng và nhận nhiều sự quan tâm là điều không tránh khỏi</p>
                            <p>Tuy nhiên, những thông tin đó xác thực và kiểm định được hay không lại là chuyện khác. Dẫu sao, Đức Huy vẫn cho thấy sự chuyên nghiệp, tập trung cùng đội bóng và cùng mọi người cố gắng để hoàn thành mục tiêu."</p>
                            <figure class="post-thumb"><img src="img/thumb/duchuy2.jpg" alt=""></figure>
                            <p>Ông nói thêm: "Tôi rất tôn trọng Đức Huy bởi sự chuyên nghiệp và những đóng góp thầm lặng của cậu ấy. Với cá tính của Huy, môi trường nào cũng phát triển được sự nghiệp. Hà Nội FC có môi trường phát triển tốt và giúp Đức Huy phát triển được khả năng. Tất nhiên, mỗi người có lựa chọn riêng. Tôi tin cậu ấy sẽ có sự lựa chọn hợp lý.”</p>

