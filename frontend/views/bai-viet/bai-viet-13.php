                            <p>Đình Hoàng và Ngọc Toàn là hai cầu thủ trụ cột của SLNA ở mùa giải năm nay. Bộ đôi này đã thi đấu lần lượt 24 và 25 trận cho đội bóng đang xếp thứ 5 trên bảng xếp hạng V.League 2019.</p>
                            <p>SLNA hiện tại vẫn chưa tìm được nhà tài trợ cho mùa giải năm sau nên 14 cầu thủ trong diện hết hạn hợp đồng buộc phải tìm kiếm bến đỗ mới.</p>
                            <figure class="post-thumb"><img src="img/thumb/cnshbdanang1.jpg" alt=""> <center><p class="caption-text">Hậu vệ Trần Đình Hoàng chuẩn bị gia nhập SHB Đà Nẵng sau khi V.League 2019 khép lại</p></center></figure>
                            <p>Theo Báo Nghệ An, Đình Hoàng và Võ Ngọc Toàn là những cầu thủ đầu tiên được xác nhận sẽ sớm ra mắt CLB mới ngay trong tuần tới. Điểm đến của họ sẽ là SHB Đà Nẵng của HLV Lê Huỳnh Đức. Rời đi theo diện tự do, cả hai không khiến CLB mới trả phí chuyển nhượng. Bên cạnh đó, họ cũng nhận được khoản lót tay không dưới 1 tỷ đồng/ năm kèm hợp đồng dài hạn.</p>
                            <p>Mùa này, thành phần thi đấu của đội bóng sông Hàn cũng có hai cầu thủ gốc Nghệ gồm Âu Văn Hoàn và Phạm Mạnh Hùng. Tuy nhiên cả hai đều ít được sử dụng và nhiều khả năng bị thanh lý sớm.</p>
                            <figure class="post-thumb"><img src="img/thumb/cnshbdanang2.jpg" alt=""> <center><p class="caption-text">Võ Ngọc Toàn (áo vàng) nối gót đàn anh Trần Đình Hoàng để gia nhập SHB Đà Nẵng.</p></center></figure>
                            <p>Nhiều năm nay, gia hạn hợp đồng với các trụ cột luôn là vấn đề nhức nhối đối với Ban lãnh đạo CLB SLNA. Ngoài bộ đôi nói trên, những tên tuổi như tiền vệ Hồ Khắc Ngọc, Thế Cường thủ môn Trần Nguyên Mạnh,... cũng sẽ sớm rời đội bóng xứ Nghệ sau khi mùa giải năm nay khép lại.</p>
