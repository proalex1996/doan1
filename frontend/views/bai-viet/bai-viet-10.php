                            <h1 class="post-title">Lãnh đạo Hà Nội FC hé lộ quá trình thương thảo hợp đồng đưa Văn Hậu sang SC Heerenveen</h1>

                            <h5>(VTC News) - Lãnh đạo đội bóng ĐKVĐ V-League chỉ khẳng định, trong thương vụ Văn Hậu sang SC Heerenveen, giá trị kinh tế không phải là ưu tiên hàng đầu.</h5>
                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2017 <span class="hidden-lst">at 8:40AM MST</span></time>
                                </div>

                            </div>
                        </div>
                        <p><span class="drop-caps">T</span>heo lãnh đạo Hà Nội FC, quá trình thương thảo đưa Văn Hậu tới Hà Lan thi đấu cho SC Heerenveen kéo dài hơn 1 tháng với các buổi thương lượng kéo dài. Suốt quá trình "đấu trí" ấy, Hà Nội FC (thuộc Công ty Cổ phần thể thao Hà Nội T&T) là đại diện hợp pháp duy nhất của Văn Hậu.</p>
                        <p>Một số điều khoản phía đối tác Hà Lan đưa ra, ban đầu, chưa thực sự phù hợp, khiến đại diện của Hà Nội FC nhiều lần yêu cầu làm rõ, để bảo đảm quyền lợi nhất cho Văn Hậu. Ví dụ: Việc phải được ra sân thi đấu một số lượng trận nhất định, các yêu cầu về chỗ ăn, ở, đi lại, nghỉ hè, được về thi đấu SEA Games 30... đều mất khá nhiều thời gian để trao đổi qua lại.</p>
                        <figure class="post-thumb"><img src="img/thumb/vanhau3.jpg" alt=""> <center><p class="caption-text"> Văn Hậu tham gia sự kiện Strong Việt Nam tại trường THCS Phù Đổng cùng các thành viên Hà Nội FC. (Ảnh: Duy Thành)</p></center></figure>

                        <div class="entry-content">
                            <div class="news-auth pull-right">
                                <div class="auth-des">
                                    <figure>
                                        <img src="img/auth/tacgia.jpg" width="50" height="50" alt="">
                                    </figure>
                                    <h5>Tính Trần</h5>
                                    <h6>Tác Giả</h6>
                                </div>
                                <div class="auth-links social--color">
                                    <a class="social__facebook" href="#"><i class="fa fa-facebook"></i>Facebook</a>
                                    <a class="social__twitter" href="#"><i class="fa fa-twitter"></i>Twitter</a>
                                    <a class="social__google-plus" href="#"><i class="fa fa-google-plus"></i>Google Plus</a>
                                </div>
                            </div>

                            <p>Nhiều thời điểm, hai bên không có được tiếng nói chung khiến việc hợp đồng tưởng chừng như rơi vào bế tắc. Thế nhưng, với mong muốn tạo điều kiện cho hậu vệ trái đội tuyển Việt Nam có điều kiện ra nước ngoài thi đấu, phát triển tài năng, công ty Cổ phần thể thao Hà Nội T&T vẫn kiên nhẫn đàm phán với đối tác cho đến ít giờ trước khi thị trường chuyển nhượng châu Âu đóng cửa thì hợp đồng hoàn tất. Các thỏa thuận đạt được sự đồng thuận chung, Văn Hậu đủ điều kiện sang khoác áo SC Heerenveen.</p>

                            <p>Theo thỏa thuận, chi tiết hợp đồng cho mượn của Đoàn Văn Hậu sẽ không được công bố ra bên ngoài.</p>

                            <p>Là đơn vị hợp pháp duy nhất quản lý cầu thủ, Hà Nội FC tuyên bố tôn trọng các thỏa thuận với đối tác nước ngoài, phù hợp với thông lệ quốc tế. Lãnh đạo đội bóng ĐKVĐ V-League chỉ khẳng định, trong thương vụ này, giá trị kinh tế không phải là ưu tiên hàng đầu.</p>
                            <p>Ngoài ra, như Chủ tịch HĐQT kiêm TGĐ T&T Group Đỗ Quang Hiển trả lời báo chí cách đây ít ngày, việc Văn Hậu về thi đấu cho U23 Việt Nam tại SEA Games 30 là điều khoản bắt buộc. Đây là điều khoản tốn nhiều công sức nhất trong việc đàm phán.</p>

                            <p>Đến nay, có thể khẳng định, chỉ cần HLV Park Hang Seo triệu tập Đoàn Văn Hậu về thi đấu SEA Games 30, cầu thủ này sẽ có mặt thực hiện nghĩa vụ quốc gia.</p>
                            <figure class="post-thumb"><img src="img/thumb/hanoifc.jpg" alt=""> <center><p class="caption-text">Chủ tịch Hà Nội FC Nguyễn Trọng Chiến lần đầu xuất hiện trước truyền thông để trả lời những vấn đề liên quan đến thương vụ Văn Hậu sang Hà Lan. (Ảnh: Duy Thành)</p></center></figure>
                            <p>Liên quan đến thông tin có công ty Việt Nam thay SC Heerenveen trả lương, thưởng, phí chuyển nhượng trong thương vụ Văn Hậu, trả lời phỏng vấn VTC News, Chủ tịch Hà Nội FC Nguyễn Trọng Chiến phủ nhận hoàn toàn. Ông Chiến nhấn mạnh rằng, toàn bộ chi phí mượn Văn Hậu đều do SC Heerenveen chi trả, không thể có chuyện được "giúp sức" từ doanh nghiệp bí mật tại Việt Nam.</p>



                            <p>Theo kế hoạch, Văn Hậu sẽ sang Hà Lan hội quân cùng CLB mới vào 10-11/9, sau giai đoạn tập trung của các ĐTQG. </p>
