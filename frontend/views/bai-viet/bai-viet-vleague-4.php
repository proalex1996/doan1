                            <h1 class="post-title">
                                VFF ra tay "cứu cánh" 4 đội V-League mùa giải 2020
                            </h1>

                            <h5>Theo đó, 4 CLB gồm Hải Phòng, Nam Định, Sông Lam Nghệ An và Thanh Hóa đã được VFF cấp phép ngoại lệ trong mùa giải 2020.</h5>

                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
                                </div>

                            </div>
                        </div>

                        <p><span class="drop-caps">L</span>iên đoàn bóng đá Việt Nam (VFF) vừa diễn ra Hội nghị BCH VFF lần thứ 4 khóa VIII (nhiệm kỳ 2019-2022). Buổi họp đã chốt lại được 10 thống nhất bao gồm: BCH đã đồng ý cấp phép ngoại lệ cho 4 CLB Hải Phòng, Sông Lam Nghệ An, Nam Định và Thanh Hóa tham dự giải bóng đá V-League 2020; Quy hoạch số lượng các đội thạm dự V-League là 14 và hạng nhất là 12; Tổ chức Cup Quốc gia U15 và U17 từ năm 2020; Thông qua các dự án đầu từ của LĐBĐ quốc tế và AFC năm 2020. </p>
                        <figure class="post-thumb"><img src="img/thumb/vff.jpg" alt=""><center><p class="caption-text">Hội nghị BCH VFF lần thứ 4 khóa VIII (nhiệm kỳ 2019-2022)</p></center></figure>
                        <div class="entry-content">
                            <div class="news-auth pull-right">
                                <div class="auth-des">
                                    <figure>
                                        <img src="img/auth/tacgia.jpg" width="50" height="50" alt="">
                                    </figure>
                                    <h5>Tính Trần</h5>
                                    <h6>Tác Giả</h6>
                                </div>
                                <div class="auth-links social--color">
                                    <a class="social__facebook" href="#"><i class="fa fa-facebook"></i>Facebook</a>
