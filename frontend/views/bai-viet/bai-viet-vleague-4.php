                            <p>Nhất trí thông qua kế hoạch báo cáo hoạt dộng của các ĐTQG trong đó trong tâm là vòng loại 2 World Cup và SEA Games 30; Đồng ý triển khai sửa đổi quy chế bóng đá chuyên nghiệp phù hợp thực tiễn; Giao cho ban thư ký tổng hợp nội dung chưa phù hợp trong điều lệ để có cơ sở xin ý kiến trong đại hội VFF tới đây; Thống nhất địa điểm, thời gian tổ chức đại hội thường niên; Báo cáo tổ chức các giải chuyên nghiệp và thống nhất phối hợp với AFF tổ chức giải đấu giữa các đội CLB vô địch các giải League năm 2020.</p>
                            <p>Trong số 10 nội dung được thông nhất trên, giới truyền thông đã bày tỏ sự quan tâm tới nguyên nhân 4 CLB Hải Phòng, Sông Lam Nghệ An, Nam Định và Thanh Hóa phải cấp phép ngoại lệ. </p>
                            <p>Theo Tổng thư ký VFF Lê Hoài Anh lí giải, cả 4 CLB trên đều không đáp ứng đủ các tiêu chí của AFC nên VFF phải có những động thái cấp phép ngoại lệ.</p>

                            <figure class="post-thumb"><img src="img/thumb/vff1.jpg" alt=""></figure>
                            <figure class="post-thumb pull-left"><img src="img/thumb/vff2.jpg" alt="" ></figure>
                            <figure class="post-thumb pull-right"><img src="img/thumb/vff3.jpg" alt=""><center><p class="caption-text text-center">4 CLB Hải Phòng, Sông Lam Nghệ An, Nam Định và Thanh Hóa phải cấp phép ngoại lệ</p></center></figure>

                            <p>"VFF đã triển khai công tác cấp phép ngoại lệ từ 2014 theo quy định của AFC. Hiện AFC đã triển khai hệ thống cấp phép trực tuyến, các CLB tham gia các giải bóng đá chuyên nghiệp của quốc gia buộc phải upload các tài liệu liên quan đến tiêu chí để bên phía AFC kiểm soát  toàn bộ nội dung và thực hiện thời hạn cấp phép với các CLB"- ông Lê Hoài Anh dẫn giải.</p>
                            <p>Theo đó, 4 CLB trên có những tiêu chí chưa đáp ứng với việc tham dự các giải vô địch. Trong đó có những tiêu chí bắt buộc về cơ sở vật chất như hệ thống chiếu sáng, sân vận động.. các tiêu chuẩn về tổ chức, chứng minh các hoạt động như tài chính cũng như các chương tròm phát triển bóng đá trẻ….</p>
                            <p>"Có những tiêu chí như chương trình phát triển bóng đá trẻ, các kế hoạch, hoạt động về cộng đồng của CLB. Thực tế đây không phải làm khó mà là quy định của AFC trong việc đảm bảo chất lượng gồm cơ sở vật chất của CLB. Thêm vào đó là CLB không được có khoản nợ quá hạn, khoản nợ lên cầu thủ, thuế. Cuối cùng là tiêu chí về nhân lực và pháp lí, đảm bảo CLB có được nhân sự phù hợp đáp ứng tiêu chí"- ông Lê Hoài Anh cho hay.</p>
                            <p>Được biết, không chỉ một số đội thuộc giải V-League gặp khó khăn, mà một số quốc gia khác cũng đang gặp vấn đề tương tự. Tuy nhiên, BCH VFF cũng đã có những ý kiến để làm sao không còn tình trạng cấp phép ngoại lệ. Dẫu vậy, việc này cũng cần phải có lộ trình.</p>
                           <p>"Các CLB cũng có những khó khăn về việc nâng cấp, sửa chữa cơ sở vật chất vì vấn đề quản lý thuộc vào địa phương nên việc nâng cấp sửa chữa cũng vướng vào kế hoạch của tỉnh có liên quan. Bản thân CLB cũng mong muốn các tỉnh thành phố sẽ ủng hộ để có điều kiện phát triển trong bối cảnh đòi hỏi về mặt hình ảnh ngày càng cao. Tôi muốn truyền tải 1 thông điệp rằng chúng ta cố gắng đảm bảo cơ sở vật chất được nâng cấp. Dù không đầu tư nhiều nhưng đủ chức năng, sạch sẽ, không có rác cho môi trường bóng đá văn minh để khi mọi người đến cảm thấy yêu thích và muốn đến hơn"- ông Hoài Anh nói.</p>
