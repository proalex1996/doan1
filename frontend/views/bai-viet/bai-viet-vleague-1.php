                        <p><span class="drop-caps">N</span>gày 17/7 là cột mốc phân chia hai nửa đối nghịch nhau của Thanh Hóa ở V-League 2019. Trước mốc này, đội bóng xứ Thanh sở hữu chuỗi 10 trận bất bại, chen chân vào top 3, và thậm chí còn nguyên hy vọng cạnh tranh chức vô địch. Nhưng thất bại 2-4 trên sân Thiên Trường đã kết thúc chuỗi ngày tươi đẹp của họ, mở ra 10 trận liên tiếp không thắng, để rồi hôm nay, Thanh Hoá bước vào vòng đấu cuối cùng với nỗi lo phải nhận vé xuống hạng.</p>
                        <p>Trong trận thua Viettel 1-3 hôm 19/10, khán giả Thanh Hóa chán nản bỏ về gần hết khi trận đấu còn tới 40 phút. Họ chán ngán với màn trình diễn của đội nhà, và chỉ trích bầu Đệ vì muốn can thiệp vào chuyên môn mà đẩy HLV Nguyễn Đức Thắng ra đi, dẫn tới đội nhà rơi vào cảnh khó khăn. HLV Vũ Quang Bảo, Mai Xuân Hợp rồi Hoàng Thanh Tùng thay nhau nắm quyền chỉ đạo trong vòng hơn ba tháng qua, nhưng không thể chặn được đà lao dốc.</p>
                        <p>Thất bại dưới tay Viettel ở vòng áp chót cuối tuần trước khiến Thanh Hóa hết cơ hội giành vị trí an toàn. Điểm bấu víu của họ lúc này chỉ còn là giành vé đá play-off với Phố Hiến - đội Á quân giải Hạng nhất - để nuôi hy vọng trụ hạng.</p>
                        <figure class="post-thumb"><img src="img/thumb/truhang.jpg" alt=""><center><p class="caption-text">Từ vị thế cạnh tranh top 3 đầu mùa, Thanh Hóa tụt dốc không phanh và đứng trước bờ vực trong cuộc đua trụ hạng.</p></center></figure>
                        <div class="entry-content">
