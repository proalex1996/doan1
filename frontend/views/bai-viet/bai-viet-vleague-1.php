                            <h1 class="post-title">Cuộc đua trụ hạng đốt nóng vòng cuối V-League 2019</h1>

                            <h5>Trước vòng đấu hạ màn hôm nay 23/10, câu hỏi duy nhất chưa có lời giải ở V-League mùa này là Thanh Hóa hay Khánh Hòa phải xuống hạng.</h5>

                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
                                </div>

                            </div>
