                            <h1 class="post-title">Dấu chấm hết cho HLV Simon McMenemy?</h1>
                            <h5>VietTimes -- Sau trận thua Thái Lan 0-3, cổ động viên Indonesia đã nhất loạt
                                lên tiếng yêu cầu LĐBĐ Indonesia sa thải HLV Simon McMenemy. Với tình thế như hiện nay,
                                số phận ông thầy Scotland khó có thể tồn tại lâu cùng ĐT xứ Vạn đảo.</h5>
