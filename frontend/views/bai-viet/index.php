                        <div class="entry-content">
                            <p><span class="drop-caps">H</span>ồi đầu năm, LĐBĐ Indonesia (PSSI) đã quyết định bổ nhiệm ông Simon McMenemy làm huấn luyện viên trưởng đội tuyển quốc gia Indonesia cho đến tháng 12 năm 2020. HLV Simon McMenemy phải đối mặt với mục tiêu rất lớn từ PSSI. Cụ thể, cựu HLV Đồng Tâm Long An sẽ phải giúp ĐT Indonesia bứt phá trên bảng xếp hạng của FIFA và tiến tới vô địch AFF Cup vào năm sau.</p>
                            <h6>Chỉ đánh giá cao Thái Lan và Malaysia</h6>
                            <div class="news-auth pull-right">
                                <div class="auth-des">
                                    <figure>
                                        <img src="img/auth/tacgia.jpg" width="50" height="50" alt="">
                                    </figure>
                                    <h5>Tính Trần</h5>
                                    <h6>Tác Giả</h6>
                                </div>
