                            <p>Tại buổi lễ ra mắt, Tổng thư ký PSSI, Queen Tisha tuyến bố: "Mục tiêu của ông Simon trong năm 2019 sẽ là cải thiện thứ hạng của ĐT Indonesia trên bảng xếp hạng FIFA. Theo đó, mục tiêu của đội bóng là lọt vào top 120 vào cuối năm 2020. Ngoài ra, PSSI hi vọng mục tiêu dài hạn của đội là lên ngôi vô địch tại AFF Suzuki Cup 2020".</p>

                            <p>HLV McMenemy từng dẫn dắt tuyển Philippines đá AFF Cup 2010 và đánh bại tuyển Việt Nam 2-0 tại Mỹ Đình ở vòng bảng, đó là lần đầu tiên trong lịch sử Philippines có mặt ở bán kết giải đấu khu vực.</p>

                            <p>Sau đó HLV McMenemy sang ĐT Long An làm HLV một thời gian rồi sang Indonesia dẫn dắt các CLB, trong đó dài nhất là làm việc ở CLB Mitra Kukar đá giải ngoại hạng Indonesia.</p>

                            <p>Phát biểu tại buổi lễ ra mắt, ông Simon nói: "Tôi cảm ơn LĐBĐ Indonesia đã đặt niềm tin vào tôi. Mặc dù tôi không phải HLV được yêu thích nhất, nhưng tôi sẽ làm việc chăm chỉ để mang về thành tích cho ĐT Indonesia".</p>

                            <p>Trước thềm vòng loại World Cup 2022, HLV McMenemy bất ngờ đánh giá cao hai đối thủ Thái Lan và Malaysia trên tờ Scottish Mail: “Thái Lan là đội mạnh nhất Đông Nam Á trong nhiều năm qua. Lúc này, họ còn có HLV mới, người có thể nâng tầm đẳng cấp đội tuyển.</p>




                            <figure class="post-thumb"><img src="img/thumb/taychaiindo.jpg" alt=""> <center><p class="caption-text">CĐV Indonesia tẩy chay đội nhà.</p></center></figure>
                            <p>Với Malaysia, họ là đại kình địch của tuyển Indonesia. Cuộc đụng độ giữa hai quốc gia sẽ căng thẳng và dữ dội. Tôi mong 80.000 khán giả của Indonesia sẽ đến sân và cổ vũ chúng tôi trong trận đặc biệt ấy”.</p>
                            <p>Thực tế 2 trận đấu vừa rồi, thi đấu trên sân nhà nhưng HLV McMenemy đã quyết định dùng đội hình 4-1-4-1 được cho là cân bằng giữa tấn công và phòng thủ. Sau trận thua 2-3 trước Malaysia ông đã xem băng hình trận đấu giữa ĐT Thái Lan và Việt Nam để học tập kinh nghiệm ông Park Hang-seo nhưng rốt cuộc cũng chỉ cầm cự được 1 hiệp đấu mà thôi.</p>

                            <h4>Trận đấu đó làm thay đổi cuộc đời tôi</h4>

                            <p>Chiến lược gia 41 tuổi này chỉ nhắc đến đội tuyển Việt Nam một cách ngắn gọn: “Tôi đối mặt với tuyển Việt Nam vào năm 2010 khi dẫn dắt tuyển Philippines. Trận đấu đó làm thay đổi cuộc đời tôi”. Có vẻ như HLV McMenemy không đánh giá cao những tiến bộ của ĐT Việt Nam trong 2 năm qua dưới thời HLV Park Hang-seo.  Có vẻ như HLV Simon McMenemy cho rằng các cầu thủ nhập tịch như Yustinus Pae, Basna, Evan Dimas…sẽ giúp ông kiếm 3 điểm một cách dễ dàng.</p>
                            <p>Nói về nhiệm vụ giành vé dự World Cup 2022, HLV McMenemy cho rằng nó thật sự rất xa vời và chỉ cần giành vé dự Asian Cup 2023 là thành công rất lớn: “Chúng tôi phải thực tế với đội nhà. Đội tuyển Indonesia muốn giành được kết quả tốt để dự Asian Cup.</p>
                            <p>Còn để góp mặt ở World Cup, đây là giấc mơ xa vời. Nếu tuyển Indonesia vượt qua được vòng loại thứ hai, chúng tôi có thể phải gặp những gã khổng lồ châu lục như Nhật Bản, Australia, Qatar. Lúc đó, chỉ cần có điểm đã may lắm rồi”.</p>
                            <p>Được thi đấu 2 trận đầu tiên trên sân nhà Bung Karno chỉ phải gặp ĐT Malaysia và Thái Lan được xem là lịch thi đấu khá thuận lợi đối với thầy trò HLV McMenemy.</p>
                            <p>Nhưng trong trận đầu ra quân gặp ĐT Malaysia, 2 lần dẫn điểm nhưng họ đều không thể bảo vệ tỷ số rồi để thua ngược ở phút 90+6, mở đầu cho cuộc bao loạn của cổ động viên Indonesia. Cơn thịnh nộ của 95.000 cổ động viên chủ nhà đã trút giận lên cổ động viên Malaysia, cơn mưa gậy gộc, đá đã diễn ra.</p>
                            <p>Trong trận gặp ĐT Thái Lan, phần thì do LĐBĐ Indonesia hạn chế bán vé, đẩy giá vé lên cao, phần thì cổ động viên chỉ nhà đã “chán” đội tuyển nên chỉ non 10.000 khán giả đến sân. Nhưng khi đội nhà bại trận, những tiếng la ó kêu gọi sa thải HLV Mc Menemy vang lên khắp 4 phía khán đài. Hai trận sân nhà thua đến 6 bàn, thế là quá đủ, người dân Indonesia có lý để mà phản đối. </p>
                            <p>Trên mạng xã hội đã xuất hiện làn sóng kêu gọi HLV Simon McMenemy từ chức bởi kết quả quá yếu kém. Bên cạnh đó, họ kêu gọi HLV Luis Milla (người trốn khỏi băng ghế huấn luyện của Indonesia trước AFF Cup) trở lại băng ghế huấn luyện của đội bóng.</p>
                            <figure class="post-thumb"><img src="img/thumb/doihinhhlvindo.jpg" alt=""> <center><p class="caption-text">Đội hình ưa thích của HLV Simon McMenemy.</p></center></figure>
                            <p>Bất lợi đang chờ HLV Simon McMenemy khi lượt đấu sắp tới, Indonesia sẽ làm khách trên sân của UAE vào ngày 10/10, khả năng trắng tay rất lớn. Sau đó, họ sẽ trở về sân nhà đối đầu với đội tuyển Việt Nam vào ngày 15/10.</p>
                            <p>Chủ tịch Hiệp hội bóng đá Indonesia (PSSI) – ông Gusti Randa tiết lộ với tờ báo Bola rằng sẽ có một cuộc họp đánh giá năng lực HLV Simon McMenemy: “Chúng tôi sẽ chờ động thái từ ông McMenemy trước. Sau đó GĐKT Danurwindo sẽ báo cáo với Ủy ban điều hành PSSI. Một cuộc họp để đánh giá năng lực của HLV Mc Menemy sẽ diễn ra trong tương lai gần, bởi chúng tôi đang chờ sự trừng phạt của FIFA sau cuộc ẩu đả trong trận đấu gặp Malaysia”.</p>
                            <p>Đánh giá không cao ĐT Việt Nam nhưng rất có thể HLV Simon McMenemy sẽ phải là người ra đi trước. Nếu không có điểm trước UAE thì 100% nhà cầm quân này chắc chắn sẽ phải vắng mặt trong trận tiếp ĐT Việt Nam.</p>
