                        <p><span class="drop-caps">T</span>hanh Hoá phải đá trận play-off là bất ngờ của mùa giải 2019. Đội bóng xứ Thanh từng đứng ở nhóm đầu, có khả năng cạnh tranh huy chương trong giai đoạn đầu V-League. Nhưng từ những thay đổi ở cấp thượng tầng và cả biến động trong khu kỹ thuật, lần lượt các HLV Nguyễn Đức Thắng rồi Vũ Quang Bảo ra đi, đội bóng thi đấu ngày càng bết bát.</p>
                        <p>Khi tình thế trở nên nguy ngập, Thanh Hoá phải đưa người nhà, là cựu tuyển thủ Mai Xuân Hợp lên cầm lái. Nhưng vị HLV trẻ này không thể trục vớt "con tàu đắm". Thanh Hoá trải qua chuỗi 11 trận không biết thắng ở V-League. Từ nhóm đầu, họ rơi dần xuống vị trí áp chót và chỉ giành được vé đá play-off nhờ HAGL thắng Khánh Hoà ở lượt cuối.</p>
                        <figure class="post-thumb"><img src="img/thumb/thanhhoaphohien.jpg" alt=""><center><p class="caption-text">Thanh Hoá sẽ nhờ vào kinh nghiệm của Hoàng Đình Tùng để mong đội nhà trụ hạng. Ảnh: Anh Khoa.</p></center></figure>
