                            <p>Đối thủ của Thanh Hóa chiều nay là Phố Hiến - tân binh của giải hạng Nhất 2019 nhưng đã thi đấu rất tốt để đoạt ngôi Á quân. HLV Hứu Hiền Vinh sở hữu nhiều cầu thủ trẻ chất lượng, luôn được HLV Park Hang-seo gọi lên đội U22 như Bảo Ngọc, Martin Lò, Đức Thông. Những cái tên này, kết hợp với các đàn anh giàu kinh nghiệm như Bửu Ngọc, Xuân Nam, Duy Long, là chìa khoá để Phố Hiến chơi tốt, giành suất đá play-off chiều nay.</p>
                            <p>Phố Hiến có chút lợi thế khi giải hạng Nhất kết thúc trước một tháng, và họ có nhiều thời gian để chuẩn bị cho màn quyết đấu tại Vinh. Với sức trẻ, tâm thế tự tin, đại diện của hạng Nhất được dự báo sẽ gây nhiều khó khăn cho Thanh Hoá</p>
                            <p>Theo quy định, Thanh Hoá không được sử dụng ngoại binh để thi đấu. Đó là một tổn thất không nhỏ cho thầy trò Mai Xuân Hợp, khi mùa này họ phụ thuộc nhiều vào những Rimario, Gramoz, Stevens... Nhưng bù lại, Thanh Hóa được đánh giá cao hơn ở kinh nghiệm trận mạc và nhiều năm chinh chiến tại V-League. HLV Mai Xuân Hợp vẫn có Đình Tùng, Trọng Hùng, Văn Thắng, Hữu Dũng, Thế Dương... để thi đấu với các đàn em.</p>
                            <p>Được thi đấu ở Vinh, sân bóng của láng giềng Nghệ An, cũng là lợi thế cho Thanh Hoá. Đội bóng xứ Thanh đã chuẩn bị hàng chục chiếc xe khách để chở CĐV nhà vào phủ vàng sân Vinh. </p>
                            <figure class="post-thumb"><img src="img/thumb/thanhhoaphohien1.jpg" alt=""><center><p class="caption-text">HLV Mai Xuân Hợp sẽ trở thành cứu tinh nếu giúp bóng đá Thanh Hoá trụ hạng. Ảnh: Anh Khoa.</p></center></figure>
                            <p>Trận đấu play-off diễn ra lúc 16h00. Theo quy định, nếu hòa trong 90 phút, hai đội đã đá luân lưu 11m để xác định chủ nhân tấm vé dự V-League 2020. Trận đấu được điều hành bởi trọng tài chính người Malaysia Razlan Joffri Bin Ali.</p>
