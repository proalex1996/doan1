                            <h1 class="post-title">Thanh Hoá - Phố Hiến: Quyết đấu vì suất dự V-League
                            </h1>

                            <h5>Đội thắng trong trận play-off trên sân Vinh chiều nay 29/10 sẽ giành quyền tham dự V-League 2020.</h5>

                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
