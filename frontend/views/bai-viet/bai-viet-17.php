                            <p>Chia sẻ trong phần bình luận sau trận đấu của đài VTC, chuyên gia Vũ Mạnh Hải cho rằng là trận đấu không sôi nổi vì tính chất quá căng thẳng. "Tôi nghĩ, nói đây là trận chiến thì đúng hơn là trận đấu. Tức là các đội chiến đấu với nhau chứ không phải đọ tài với nhau về mảng miếng, kỹ thuật", ông Vũ Mạnh Hải nói.</p>
                            <p>Nhận định về kết quả hoà giữa hai đội, ông Vũ Mạnh Hải cho rằng tỉ số này đáp ứng đúng cục diện. Việc học trò của HLV Park Hang-seo thi đấu tốt nhưng không giành chiến thắng là điều dễ hiểu, đặc biệt ở việc các cầu thủ đang có dấu hiệu bị quá tải.</p>
                            <p>"Trong trận đấu này, chúng ta đã kỳ vọng rất nhiều vào các trụ cột đến từ Hà Nội FC, đặc biệt những cút sút dài, dứt khoát của Quang Hải. Song, các bạn nhiều lần để các cầu thủ Thái Lan "qua mặt" và có những pha sút nguy hiểm. Quả thật, các cầu thủ của CLB Hà Hội đã thi đấu quá tải, gây ảnh hưởng trực tiếp đến lực lượng các cầu thủ", vị chuyên gia phân tích.</p>
                            <p>Đồng quan điểm với ông Vũ Mạnh Hải, BL Quang Huy cũng cho rằng kết quả này giữa hai đội là rất hợp lý, đặc biệt trong bối cảnh Thái Lan thua "liên hoàn" trong thời gian gần đây. "Kết quả trận đấu như vậy là dễ hiểu. Nó giúp xoa dịu bớt sự căng thẳng về chuyên môn của 2 nền bóng đá, khiến cầu thủ bị áp lực và như đang đấu nhau chứ không chơi bóng với nhau. Trong khi đó, Việt Nam và Thái Lan nên là hai nền bóng đá song hành với nhau trong khu vực chứ không phải là lúc nào cũng chiến đấu với nhau", BLV Quang Huy cho hay.</p>
                            <p>Nói về cơ hội của ĐT Việt Nam trong thời gian tới, ông Vũ Mạnh Hải cho rằng chúng ta cần có một chiến dịch hợp lý cho vòng loại World Cup 2022. Để có thành tích tốt hơn, phía LĐBĐ Việt Bam cần có kế hoạch dài hơi, ưu tiên để đảm bảo ĐTQG có thời điểm thích hợp, phân tán lực lượng tránh việc bị quá tải. Cũng theo ông Vũ Mạnh Hải, trận đấu gặp Thái Lan không phải là trận đấu quyết định mà chúng ta cần đi dài đến tận năm 2020 nên việc cân bằng lực lượng để đảm bảo sức khoẻ cho các cầu thủ ĐTQG là rất cần thiết.</p>


