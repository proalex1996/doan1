                        <p><span class="drop-caps">T</span>rận chiến cân não giữa ĐT Việt Nam và ĐT Thái Lan trên sân Thammasat trong khuôn khổ vòng loại thứ hai World Cup 2022 khu vực châu Á khép lại với tỉ số hòa 0-0. Như vậy, Thái Lan và Việt Nam chia đều 1 điểm sau lượt trận đầu tiên bảng G.</p>
                        <p></p>

