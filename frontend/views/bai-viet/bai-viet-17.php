                            <h1 class="post-title">Chuyên gia chỉ ra điểm yếu của tuyển Việt Nam trong trận hòa Thái Lan</h1>

                            <h5>Chuyên gia Vũ Mạnh Hải và BLV Quang Huy đều cho rằng việc Việt Nam hoà 0-0 với Thái Lan là rất hợp lý.</h5>
                            <div class="post-meta">
                                <div class="item">
