                            <p>Còn một nhiệm vụ nữa là cùng đội U.22+2 VN lọt vào trận chung kết môn bóng đá nam SEA Games 30, ông Park cùng các cộng sự đang khẩn trương chuẩn bị. Xin được nói lại, hợp đồng cũ, VFF không yêu cầu ông Park phải giành HCV SEA Games 30 mà chỉ đặt chỉ tiêu lọt vào chung kết. Nhưng ông Park đã “thoát ly” văn bản khi trong những lần chia sẻ với báo chí gần đây, đều nhấn mạnh đến mong mỏi đoạt HCV SEA Games của người dân VN suốt 60 năm qua. Có lẽ vì hiểu được sự khát khao này mà ông đã “nâng” chỉ tiêu lên cao hơn điều khoản trong hợp đồng đã ký.</p>
                            <figure class="post-thumb"><img src="img/thumb/lehoaianh.jpg" alt=""> <center><p class="caption-text">Tổng thư ký VFF Lê Hoài Anh - người trực tiếp đàm phán với đại diện của ông Park</p></center></figure>
                            <p>Không tiết lộ cụ thể về nội dung nội dung đã, đang và sẽ đàm phán với người đại diện của ông Park nhưng lãnh đạo VFF nhất quán chủ trương: “Ưu tiên số một của quá trình đàm phán là tập trung vào sự phát triển của bóng đá VN, tiếp tục nâng vị thế của bóng đá VN trên đấu trường quốc tế mà ở đó, vai trò của HLV Park Hang-seo sẽ được nâng cấp. Trách nhiệm đặt ra cho ông Park sẽ nặng hơn, xứng đáng với chất xám, năng lực, trình độ và nguyện vọng của chính bản thân ông. Tiền bạc là một nội dung quan trọng được đôi bên đề cập đến nhưng không phải nội dung tiên quyết”. Chủ trương này này hoàn toàn phù hợp với quan điểm của ông Park: “Khi đàm phán hợp đồng, đúng là không thể không nói về chuyện tiền bạc nhưng với tôi tiền không phải tất cả. Tôi đã căn dặn rất kỹ người đại diện của mình rằng, khi đàm phán với VFF, tiền không phải là thứ quan trọng nhất. Điều quan trọng hơn cả là phải nói cho VFF hiểu vai trò, tầm nhìn, kế hoạch của tôi rồi tiền bạc mới là vấn đề tiếp theo”.</p>

