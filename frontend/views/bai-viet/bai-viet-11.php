                            <h1 class="post-title">VFF và ông Park tái khởi động đàm phán hợp đồng</h1>

                            <h5>Việc đàm phán giữa Liên đoàn Bóng đá Việt Nam (VFF) và HLV Park Hang-seo vẫn đang được những người trong cuộc giữ kín nhưng theo nguồn tin của Thanh Niên, các bên đã không còn ở tình trạng “đóng băng” mà đã khởi động trở lại.</h5>
                            <div class="post-meta">
                                <div class="item">
                                    <time datetime="2018-02-14 20:00"><i class="fa fa-clock-o"></i>January 24, 2019 <span class="hidden-lst">at 8:40AM MST</span></time>
                                </div>

                            </div>
                        </div>
                        <figure class="post-thumb"><img src="img/thumb/parkhangseo.jpg" alt=""> <center><p class="caption-text">Với HLV Park Hang-seo, tiền bạc không phải vấn đề quan trọng nhất.</p></center></figure>
                        <p><span class="drop-caps">G</span>iữa tháng 7 vừa qua, công ty đại diện của ông Park đã tuyên bố tạm ngưng đàm phán vì cho rằng quá trình thương thảo đã bị tác động bởi một số yếu tố khách quan. Trong suốt mấy tháng vừa qua, lãnh đạo VFF và ông Park cũng chỉ tập trung tối đa vào những công tác chuyên môn liên quan đến đội tuyển Việt Nam (VN) và đội U.22 hướng tới những mục tiêu quan trọng đến hết năm 2019. Được biết, ông Park đã hoàn thành rất xuất sắc 2/3 nhiệm vụ được giao trong bản hợp đồng đầu tiên được ký vào tháng 10.2017.</p>

                        <center><iframe width="560" height="315" src="https://www.youtube.com/embed/swfjt-jyBeQ" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe><p class = "caption-text"> HLV Park Hang-seo chia tay Văn Lâm cùng đội tuyển trở về Việt Nam sau chiến thắng trước Malaysia</p></center>

                        <div class="entry-content">
                            <div class="news-auth pull-right">
