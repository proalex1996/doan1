                            <p>Bước vào cuộc đối đầu quan trọng với “đại kình địch” Thái-lan, HLV Park Hang Seo đã có quyết định khiến nhiều người bất ngờ khi tung ra sân đội hình có thiên hướng nhiều về tấn công là 3-4-3. Trong khung gỗ là thủ môn Văn Lâm, án ngữ ngay phía trên là bộ ba trung vệ Duy Mạnh – Ngọc Hải – Tiến Dũng.</p>
                            <p>Còn trên hàng tiền vệ, hai cầu thủ được thầy Park lựa chọn ra sân ngay từ đầu là Hùng Dũng và Tuấn Anh, giúp tuyến giữa của Việt Nam vừa có được sự mềm mại những vẫn giữ được độ “rắn” cần thiết trước số đông tiền vệ của Thái-lan. Trọng Hoàng đã kịp thời trở lại và ngay lập tức được đảm nhiệm hành lang cánh phải, trong khi Hồng Duy được ban huấn luyện tin tưởng đá thay cho Văn Hậu chưa có được thể trạng tốt nhất.</p>
                            <figure class="post-thumb"><img src="img/thumb/vietnamthailan1.jpg" alt=""><center><p class="caption-text">Đội hình ra sân của đội tuyển Việt Nam. (Ảnh: VFF)</p></center></figure>
                            <p>Thi đấu cao nhất trên hàng công của đội đương kim vô địch Đông - Nam Á là tiền đạo trẻ Tiến Linh chứ không phải người đồng đội tại Becamex Bình Dương – lão tướng Anh Đức. Hai “vệ tinh” hỗ trợ cho trung phong 22 tuổi đều là những cầu thủ có tốc độ và khéo léo là Quang Hải và Văn Toàn.</p>
                            <p>Dù đây là trận đấu có tính chất rất quan trọng, nhưng cả hai đội đã tỏ ra quá hiểu nhau và không ngần ngại chơi tấn công cởi mở ngay sau tiếng còi nhập cuộc. Khi thời gian chưa trôi qua giây thứ 20, đội khách đã có được tình huống nguy hiểm đầu tiên. Từ pha phát bóng dài của Hồng Duy, Văn Toàn bắt tốc độ thoát xuống rất nhanh, vượt qua hậu vệ Hemviboon trước khi tung cú dứt điểm chéo góc, nhưng đáng tiếc bóng lại đi chệch cột dọc trong gang tấc.</p>
                            <p>Áp lực liên tiếp được các cầu thủ áo đỏ tạo ra. Phút thứ 3, tới lượt Quang Hải có cơ hội từ quả đá phạt bên phía cánh phải, quỹ đạo bóng đi rất khó chịu và thủ môn đội trưởng Siwarak phải khá vất vả để đấm bóng ra. Đội tuyển Thái-lan cho thấy những dấu hiệu căng thẳng và có phần căng cứng bởi áp lực, trong khi các học trò của HLV Park càng đá càng tự tin dù phải đá trên sân khách.</p>
                            <p>Sau quãng thời gian đầu hơi lép vế, các cầu thủ Thái-lan dần lấy lại bình tĩnh và bắt đầu cầm được nhiều bóng hơn. Đội bóng của HLV Akira Nishino có cơ hội đáng chú ý từ quả đánh đầu sau tình huống được hưởng phạt góc của Pansa Hemviboon, tuy nhiên không đủ hiểm hóc để đánh bại thủ thành Văn Lâm.</p>
                            <p>Sau những tình huống ép sân qua lại trong hơn nửa đầu hiệp 1, quãng thời gian sau đó chứng kiến thế trận cân bằng, những pha bóng nguy hiểm chủ yếu đến từ pha bóng dài, đơn giản bên phía Thái-lan. Lần lượt ở các phút 20 và 35, cơ hội liên tục được trao cho Thitiphan nhưng đều là những cú đánh đầu đi chệch cột dọc của tiền vệ đội chủ nhà.</p>

                            <p>Ba phút trước khi hiệp 1 khép lại, cơ hội ngon ăn thực sự rõ nét suýt chút nữa được Supachok cụ thể hóa, sau tình huống đột phá bên phía cánh trái rồi “bẻ lưng” trung vệ Duy Mạnh trước khi dứt điểm đi hơi cao hơn so với khung thành của Văn Lâm. Tỷ số hòa không bàn thắng cũng là kết quả sau 45 phút đầu tiên. Cả hai ngôi sao rất được kỳ vọng của hai đội là Quang Hải và Chanathip đều bị phong tỏa rất chặt và hầu như không có nhiều “đất diễn” trong hiệp đấu này.</p>
                            <figure class="post-thumb"><img src="img/thumb/vietnamthailan2.jpg" alt=""><center><p class="caption-text">Cũng giống như Chanathip bên phía đội chủ nhà, tiền vệ Quang Hải cũng được các cầu thủ phòng ngự Thái-lan "chăm sóc" rất kỹ lưỡng.</p></center></figure>

                            <p>Trở lại sau giờ nghỉ, HLV Park có sự điều chỉnh ngay đầu hiệp 2 khi tung Công Phượng vào sân và rút Tiến Linh ra nghỉ. Tuy nhiên, Thái-lan vẫn là đội tạo được tình huống nguy hiểm. Phút 51, các cầu thủ chủ nhà phối hợp khá nhuần nhuyễn, kết thúc bằng cú sút chân trái ở góc hẹp nhưng thủ môn Văn Lâm đã kịp thời đổ người cản phá.</p>
                            <p>Trong khoảng 15 phút đầu hiệp 2, đội bóng của HLV Akira Nishino liên tiếp gia tăng sức ép, nỗ lực tạo ra nhiều pha dứt điểm nguy hiểm. Số đông tiền vệ bên phía Thái-lan giúp họ có được không ít lợi thế ở khu vực trung tuyến. Ở chiều ngược lại, sự nguy hiểm ở những đường tấn công đến từ tốc độ và sự ăn ý của bộ đôi Văn Toàn - Công Phượng, tuy vậy hai người bạn thân thuộc biên chế CLB Hoàng Anh Gia Lai đều không thể tận dụng tốt ở những pha dứt điểm quyết định cuối cùng.</p>
                            <figure class="post-thumb"><img src="img/thumb/vietnamthailan3.jpg" alt=""><center><p class="caption-text">Các cầu thủ thiên hướng chơi kỹ thuật như Tuấn Anh liên tục bị đội tuyển Thái-lan hạn chế bằng những pha phạm lỗi khá ác ý.</p></center></figure>
                            <p>Càng trôi về cuối trận, các cầu thủ của cả Thái-lan và Việt Nam không còn duy trì được thể lực tốt, những pha phối hợp diễn ra với tốc độ chậm đi trông thấy và cũng ít nguy hiểm hơn. Pha bóng “thót tim” ở phút bù giờ cuối cùng của đội chủ nhà cũng là điểm nhấn khép lại trận đấu trên sân Thammasat. Cầu thủ chơi năng nổ nhất trên hàng công Thái-lan là Supachok thoát xuống rất nhanh sau pha chọc khe của Chanathip, đi bóng vượt qua cả Văn Lâm nhưng cú sút quyết định ở sát đường biên ngang lại không chiến thắng được hậu vệ phòng ngự bên phía Việt Nam.</p>
                            <p>Tỷ số hòa 0-0 trong trận cầu hao tổn rất nhiều về thể lực cho thấy bóng đá Thái-lan vẫn còn nhiều điều phải cải thiện dưới thời HLV mới, trong khi một điểm đầu tay tại vòng loại 2 World Cup 2022 ngay trên sân khách là kết quả không tồi với thầy trò HLV Park.</p>
                            <p>Trận đấu tiếp theo của đội tuyển Việt Nam tại bảng G sẽ là cuộc tiếp đón Malaysia trên sân nhà Mỹ Đình vào ngày 10-10.</p>
                            <p>* Ở trận đấu còn lại kết thúc không lâu sau đó, đội tuyển Malaysia có chiến thắng 3-2 quý giá trên sân của Indonesia nhờ bàn thắng ở phút bù giờ thứ 6, qua đó tạm vươn lên ngôi đầu bảng G. Đội tuyển còn lại của bảng đấu là UAE sẽ ra quân muộn hơn, ở lượt đấu thứ 2 trên sân của Malaysia sẽ diễn ra vào ngày 10-9 tới.</p>
