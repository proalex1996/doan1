                        <figure class="post-thumb"><img src="img/thumb/vietnamthailan.jpg" alt=""><center><p class="caption-text">Tiền vệ sáng tạo Chanathip (10) liên tục bị các cầu thủ bên phía Việt Nam theo kèm rất gắt gao</p></center></figure>
                        <p><span class="drop-caps">T</span>uyển Việt Nam vừa có trận hòa không bàn thắng với hàng loạt tình huống quyết liệt, hấp dẫn khi đối đầu Thái Lan ở vòng loại thứ 2 World Cup 2022 khu vực châu Á.</p>
                        <p></p>

