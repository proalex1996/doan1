                            <h1 class="post-title">Việt Nam - Thái-lan: Bất phân thắng bại</h1>

                            <h5>NDĐT – Dù phải thi đấu trên sân khách, tuy nhiên các cầu thủ Việt Nam đã có một trận đấu kiên cường, tuân thủ chặt chẽ chiến thuật của ban huấn luyện và giành được một điểm đầu tay sau trận hòa 0-0 trước Thái-lan tại Bảng G Vòng loại 2 World Cup 2022 khu vực châu Á diễn ra trong tối 5-9.</h5>
